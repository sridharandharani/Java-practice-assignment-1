//wtp to check whether a number is divisible by 8 or not
public class Divisibleby8 {
    public static void main(String[] args) {
        Integer a = 16;
        if (a % 8 == 0)
        {
            System.out.println("It is divisible");
        }
        else
        {
            System.out.println("not divisible ");
        }
    }
}
